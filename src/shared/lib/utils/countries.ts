export type { CountryMeta };

export function getCountryData(): CountryMeta[] {
	return COUNTRY_DATA;
}

export function getCountries(): string[] {
	return COUNTRY_DATA.map((c) => c.name);
}

export function getCountryByName(name: string): CountryMeta | undefined {
	return COUNTRY_DATA.find((c) => c.name.toLowerCase() === name.toLowerCase());
}

export function getCountryByIso2(iso2: string): CountryMeta | undefined {
	return COUNTRY_DATA.find((c) => c.iso2.toLowerCase() === iso2.toLowerCase());
}

export function getCountrySelectData(): { value: string; label: string }[] {
	return COUNTRY_DATA.map((c) => ({
		value: c.name,
		label: `${c.flag} ${c.name}`,
	}));
}

export function getPhoneCodeSelectData(): { value: string; label: string }[] {
	return COUNTRY_DATA.map((c) => ({
		value: c.phoneCode,
		label: `${c.flag} ${c.name} (${c.phoneCode})`,
	}));
}

export function normalizeNationality(
	value: string | null | undefined
): string | null {
	if (!value) return null;
	const trimmed = value.trim();
	if (!trimmed) return null;
	const key = trimmed.toLowerCase();
	return NATIONALITY_MAP[key] ?? null;
}
